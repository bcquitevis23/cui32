#define USB_DRIVE 0

#include "HardwareProfile.h"
#include "fatfs/ff.h"
#include "USB/usb.h"


int main (void)
{
    SYSTEMConfigPerformance(GetSystemClock());
    INTEnableSystemMultiVectoredInt();

    USBInitialize(0);
    int MediaDetect;
    do {
        USBTasks();
        MediaDetect = USBHostMSDSCSIMediaDetect();
    } while (!MediaDetect);

    FATFS fatfs[_VOLUMES];
    f_mount(USB_DRIVE, &fatfs[USB_DRIVE]);
    FRESULT res;
    FILINFO fno;
    DIR dir;
    char *fn;
#if _USE_LFN
    static char lfn[_MAX_LFN * (_DF1S ? 2 : 1) + 1];
    fno.lfname = lfn;
    fno.lfsize = sizeof(lfn);
#endif

    FIL fp;
    res = f_open(&fp, "test.txt", FA_CREATE_ALWAYS | FA_WRITE);
    res = f_puts("Hello World!\näöü\n", &fp);
    res = f_opendir(&dir, "");
    if (res == FR_OK) {
        for (;;) {
            res = f_readdir(&dir, &fno);
            if (res != FR_OK || fno.fname[0] == 0) break;
            if (fno.fname[0] == '.') continue;
#if _USE_LFN
            fn = *fno.lfname ? fno.lfname : fno.fname;
#else
            fn = fno.fname;
#endif
            if (fno.fattrib & AM_DIR) {
                res = f_puts("\nDIR ", &fp);
            } else {
                res = f_puts("\nFIL ", &fp);
            }
            res = f_puts(fn, &fp);
        }
    }
    res = f_close(&fp);

    LATE = 0xFFFE; TRISE = 0xFFFE;
    LATEbits.LATE0 = 0;

    while (1) {
    }
}

